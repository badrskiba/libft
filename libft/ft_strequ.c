#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	if (!s1 && !s2)
		return (1);
	i = ft_strcmp(s1, s2);
	if (i != 0)
		return (0);
	else
		return (1);
}
